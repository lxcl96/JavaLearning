/*
    inner class:内部类
    一个类的内部又定义了另一个类的。则内部的类叫内部类（inner class）， 外面的类叫外部类（outer class） 是我们类的第五大成员
        类的五大成员：属性，方法，构造器，代码块，内部类

    特点：
    内部类的最大特点就是可以直接访问私有属性，并且可以体现类与类之间的包含关系

    基本语法：
    class Outer {  //外部类
        class inner {  //内部类

        }
    }

    class Other {} //与外部类同级的叫：外部其他类
 */