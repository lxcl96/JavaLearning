/*
    因为数组的缺点引入集合：
        1、数组长度开始时必须指定，而且一旦指定无法修改
        2、数组保存的数据元素必须为同一类型
        3、数组增删元素特别复杂



    集合：
        1、可以动态的保存任意多个对象，使用比较方便
        2、提供了一系列操作对象的方法，add、remove、set、get等
        3、使用集合代码简洁
 */